               WI( LU_ICOL2(jj) ) = WI( LU_ICOL2(jj) ) + ar*JVSI(jj) + ai*JVSR(jj)
            END DO
         END DO
         DO kk = LU_CROW2(k), LU_CROW2(k+1)-1
            JVSR(kk) = WR( LU_ICOL2(kk) )
            JVSI(kk) = WI( LU_ICOL2(kk) )
