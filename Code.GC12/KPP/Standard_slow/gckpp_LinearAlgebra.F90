SUBROUTINE KppDecompCmplxR( JVSR, JVSI, IER )
! ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!    Sparse LU factorization, complex
!   (Real and Imaginary parts are used instead of complex data type)     
! ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

  USE gckpp_Parameters
  USE gckpp_JacobianSP

      INTEGER       :: IER
      REAL(kind=dp) :: JVSR(LU_NONZERO2), JVSI(LU_NONZERO2) 
      REAL(kind=dp) :: WR(NVAR2), WI(NVAR2), ar, ai, den
      INTEGER       :: k, kk, j, jj

      IER = 0
      ar  = 0.0
      DO k=1,NVAR2
        IF (  ( ABS(JVSR(LU_DIAG2(k))) < TINY(ar) ) .AND. &
              ( ABS(JVSI(LU_DIAG2(k))) < TINY(ar) ) )  THEN
            IER = k
            RETURN
