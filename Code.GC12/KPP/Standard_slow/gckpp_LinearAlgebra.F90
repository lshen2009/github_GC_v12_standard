        DO kk = LU_CROW2(k), LU_CROW2(k+1)-1
              WR( LU_ICOL2(kk) ) = JVSR(kk)
              WI( LU_ICOL2(kk) ) = JVSI(kk)
        END DO
        DO kk = LU_CROW2(k), LU_DIAG2(k)-1
            j = LU_ICOL2(kk)
            den = JVSR(LU_DIAG2(j))**2 + JVSI(LU_DIAG2(j))**2
            ar = -(WR(j)*JVSR(LU_DIAG2(j)) + WI(j)*JVSI(LU_DIAG2(j)))/den
